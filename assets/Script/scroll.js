cc.Class({
    extends: cc.Component,

    properties: {
        speed: 0,
        border: 0,
        origin: 0,
    },

    onLoad: function(){

    },

    update: function(dt){
        if(!GlobalGame.gameOn)
            return;
        let x = this.node.x;
        x -= this.speed * dt;
        if(x <= this.border)
            x = this.origin;
        this.node.x = x;
    },

});
